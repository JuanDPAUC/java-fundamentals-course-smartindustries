// Paso 1: Toda clase en Java empieza con "public class" seguido del nombre.
// El nombre de la clase debe ser exactamente igual al nombre del archivo (HolaMundo.java).
public class HolaMundo {
 
    // Paso 2: El método main() es el punto de entrada del programa.
    // Cuando ejecutas un programa en Java, esto es lo primero que se corre.
    public static void main(String[] args) {
 
        // Paso 3: System.out.println() es la instrucción que usamos para
        // mostrar (imprimir) un mensaje de texto en la consola.
        // Todo lo que va entre comillas " " es el texto que se mostrará.
        System.out.println("Hola Mundo");
 
        // Paso 4: Cada instrucción en Java termina con punto y coma ( ; ).
        // Si lo olvidas, el programa no va a compilar.
 
    } // Paso 5: Esta llave cierra el método main().
 
} // Paso 6: Esta llave cierra la clase HolaMundo.